<h2>使い方</h2>
<em
  >注意：比較的新しいChrome OS（iOS版は不可）で正常に動作します。Web Speech
  APIを用いているためです。</em
>
<ul>
  <li>
    録音ボタンを「押している間」（ボタンの色が赤系に変化します）にマイクに向かって話すと日本語が記録されます。
  </li>
  <li>場合によってはアクセス許可が必要になるので許可をお願いします。</li>
  <li>音声を記録すると一つずつリストとして表示されます。</li>
  <li>それぞれのテキストを修正できます。修正が終わったら「終了」ボタンで保存します。</li>
  <li>xを押すと消去します。</li>
  <li>一度ページを移動するとすべての記録が消えてしまうので注意してください。</li>
</ul>
